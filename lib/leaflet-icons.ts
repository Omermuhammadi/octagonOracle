import L from 'leaflet';

// Track if icons have been initialized
let iconsInitialized = false;

// Fix Leaflet icon issues with Next.js
export function initializeLeafletIcons() {
  // Only initialize once
  if (iconsInitialized) return;
  
  // Make sure we're in browser environment
  if (typeof window === 'undefined') return;
  
  // Reset the default icon settings
  delete L.Icon.Default.prototype._getIconUrl;
  
  // Set new paths to icon assets via CDN
  L.Icon.Default.mergeOptions({
    iconRetinaUrl: 'https://unpkg.com/leaflet@1.7.1/dist/images/marker-icon-2x.png',
    iconUrl: 'https://unpkg.com/leaflet@1.7.1/dist/images/marker-icon.png',
    shadowUrl: 'https://unpkg.com/leaflet@1.7.1/dist/images/marker-shadow.png',
  });
  
  // Mark as initialized
  iconsInitialized = true;
}

